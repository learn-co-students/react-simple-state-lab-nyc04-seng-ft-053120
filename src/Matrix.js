import React, { Component } from 'react';

import Cell from './Cell'

export default class Matrix extends Component {
  
  // Maps each row to create an array of divs with className "cell"
  genRow = (vals) => {
    return vals.map(val => < Cell value={val} />)
  }
  
  // Instance method that maps this.props.values to an array of JSX
  // Every 'row' (nested array) will create a <div> with className "row"
  genMatrix = () => {
    return this.props.values.map(rowVals => <div className="row">{this.genRow(rowVals)}</div>)
  }
  
  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }
  
}

